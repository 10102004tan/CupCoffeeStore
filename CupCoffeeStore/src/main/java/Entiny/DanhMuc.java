package Entiny;

public class DanhMuc {
	private int ma;
	private String ten;
	public int getMa() {
		return ma;
	}
	public void setMa(int ma) {
		this.ma = ma;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public DanhMuc(int ma, String ten) {
		this.ma = ma;
		this.ten = ten;
	}
	
	
}
